package lab.java.user;

public class userInfo {
private String userid;
private String userpass;
private String username;
private String userphone;
private String concernList;
private int foundMoney;
private String date;
private char advice;
private char founderYN;
private double myBuilding;


public String getUserid() {
	return userid;
}
public void setUserid(String userid) {
	this.userid = userid;
}
public String getUserpass() {
	return userpass;
}
public void setUserpass(String userpass) {
	this.userpass = userpass;
}

public String getUsername() {
	return username;
}
public void setUsername(String username) {
	this.username = username;
}
public String getUserphone() {
	return userphone;
}
public void setUserphone(String userphone) {
	this.userphone = userphone;
}
public String getConcernList() {
	return concernList;
}
public void setConcernList(String concernList) {
	this.concernList = concernList;
}
public int getFoundMoney() {
	return foundMoney;
}
public void setFoundMoney(int foundMoney) {
	this.foundMoney = foundMoney;
}
public String getDate() {
	return date;
}
public void setDate(String date) {
	this.date = date;
}
public char getAdvice() {
	return advice;
}
public void setAdvice(char advice) {
	this.advice = advice;
}
public char getFounderYN() {
	return founderYN;
}
public void setFounderYN(char founderYN) {
	this.founderYN = founderYN;
}
public double getMyBuilding() {
	return myBuilding;
}
public void setMyBuilding(double myBuilding) {
	this.myBuilding = myBuilding;
}
}


